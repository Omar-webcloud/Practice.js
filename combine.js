const manush = ["Oritro", "Dalim", "Mukul"];
console.log(manush) // ["Oritro", "Dalim", "Mukul"]

const manushNa = ["horin", "goru", "lota pata"];
console.log(manushNa); //["horin", "goru", "lota pata"]

const animals = manush.concat(manushNa);
console.log(animals); // [ 'Oritro', 'Dalim', 'Mukul', 'horin', 'goru', 'lota pata' ]